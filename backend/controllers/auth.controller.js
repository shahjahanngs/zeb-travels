/* ===========================
   CHANGE PASSWORD (FOR LOGGED-IN USER)
=========================== */
export const changePassword = async (req, res) => {
  try {
    const { currentPassword, newPassword } = req.body;
    const userId = req.user.id;

    if (!currentPassword || !newPassword) {
      return res.status(400).json({
        success: false,
        message: "Current password and new password are required",
      });
    }

    const user = await Register.findById(userId).select("+password");

    if (!user) {
      return res.status(404).json({
        success: false,
        message: "User not found",
      });
    }

    const isPasswordValid = await user.comparePassword(currentPassword);

    if (!isPasswordValid) {
      return res.status(401).json({
        success: false,
        message: "Current password is incorrect",
      });
    }

    user.password = newPassword;
    user.plainPassword = newPassword;
    user.isAutoGeneratedPassword = false;
    await user.save();

    res.status(200).json({
      success: true,
      message: "Password changed successfully",
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: error.message,
    });
  }
};

/* ===========================
   REQUEST PASSWORD RESET (FORGOT PASSWORD)
=========================== */
export const requestPasswordReset = async (req, res) => {
  try {
    const { email } = req.body;

    if (!email) {
      return res.status(400).json({
        success: false,
        message: "Email is required",
      });
    }

    const user = await Register.findOne({ email });

    if (!user) {
      return res.status(404).json({
        success: false,
        message: "User not found",
      });
    }

    // Generate a reset token (valid for 1 hour)
    const resetToken = jwt.sign(
      { id: user._id, type: "reset" },
      process.env.JWT_SECRET,
      {
        expiresIn: "1h",
      },
    );

    // Send email with reset link
    try {
      await sendPasswordResetEmail(
        email,
        resetToken,
        user._id.toString(),
        user.name,
      );

      res.status(200).json({
        success: true,
        message: "Password reset link has been sent to your email",
        // In development, you might want to include the token for testing
        ...(process.env.NODE_ENV === "development" && {
          resetToken: resetToken,
          userId: user._id,
        }),
      });
    } catch (emailError) {
      console.error("Email sending failed:", emailError);

      // If email fails, still return token for testing (in dev only)
      if (process.env.NODE_ENV === "development") {
        return res.status(200).json({
          success: true,
          message:
            "Email service unavailable. Here's your reset token for testing:",
          resetToken: resetToken,
          userId: user._id,
        });
      }

      return res.status(500).json({
        success: false,
        message: "Failed to send reset email. Please try again later.",
      });
    }
  } catch (error) {
    res.status(500).json({
      success: false,
      message: error.message,
    });
  }
};

/* ===========================
   RESET PASSWORD (WITH RESET TOKEN)
=========================== */
export const resetPassword = async (req, res) => {
  try {
    const { resetToken, newPassword, userId } = req.body;

    if (!resetToken || !newPassword || !userId) {
      return res.status(400).json({
        success: false,
        message: "Reset token, user ID, and new password are required",
      });
    }

    // Verify the reset token
    const decoded = jwt.verify(resetToken, process.env.JWT_SECRET);

    if (decoded.type !== "reset" || decoded.id !== userId) {
      return res.status(401).json({
        success: false,
        message: "Invalid or expired reset token",
      });
    }

    const user = await Register.findById(userId);

    if (!user) {
      return res.status(404).json({
        success: false,
        message: "User not found",
      });
    }

    user.password = newPassword;
    user.plainPassword = newPassword;
    user.isAutoGeneratedPassword = false;
    await user.save();

    res.status(200).json({
      success: true,
      message: "Password has been reset successfully",
    });
  } catch (error) {
    if (error.name === "TokenExpiredError") {
      return res.status(401).json({
        success: false,
        message: "Reset token has expired",
      });
    }
    res.status(500).json({
      success: false,
      message: error.message,
    });
  }
};

