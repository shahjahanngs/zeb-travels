/* ===========================
   REGISTER USER
=========================== */
export const registerUser = async (req, res) => {
  try {
    const {
      name,
      email,
      phone,
      role,
      companyName,
      address = "",
      city = "",
      password,
    } = req.body;

    const userExists = await Register.findOne({ email });
    if (userExists) {
      return res.status(400).json({
        success: false,
        message: "User already exists",
      });
    }
    const generatedPassword =
      password || Math.random().toString(36).slice(-8) + "A1!";
    const plainPassword = generatedPassword;

    const status = role === "Agency" ? "Inactive" : "Active";

    let agencyCode = null;
    if (role === "Agency") {
      const lastAgency = await Register.find({ agencyCode: { $exists: true } })
        .sort({ agencyCode: -1 })
        .limit(1);
      const lastCode = lastAgency[0]?.agencyCode || "0000";
      const nextCodeNum = parseInt(lastCode, 10) + 1;
      agencyCode = nextCodeNum.toString().padStart(4, "0");
    }

    // ZIP account will be created when agent status is activated

    const user = await Register.create({
      name,
      email,
      phone,
      password: generatedPassword,
      plainPassword,
      isAutoGeneratedPassword: true,
      role,
      companyName,
      address,
      city,
      agencyCode,
      status,
    });

    res.status(201).json({
      success: true,
      message: "User registered successfully",
      token: generateToken(user._id),
      user: {
        id: user._id,
        name: user.name,
        email: user.email,
        role: user.role,
        agencyCode: user.agencyCode,
      },
    });
  } catch (error) {
    console.error("REGISTER ERROR:", error);

    res.status(400).json({
      success: false,
      message: error.message,
    });
  }
};

/* ===========================
   LOGIN USER
=========================== */
export const loginUser = async (req, res) => {
  try {
    const {
      email,
      password,
      // agencyCode
    } = req.body;

    const user = await Register.findOne({ email }).select("+password");

    if (!user) {
      return res.status(401).json({
        success: false,
        message: "User not Found",
      });
    }

    if (!(await user.comparePassword(password))) {
      return res.status(401).json({
        success: false,
        message: "Invalid Password",
      });
    }

    // Check if user is an Agency and verify their status
    if (user.role === "Agency" && user.status !== "Active") {
      const msg =
        user.status === "Suspended"
          ? "Your account is suspended. Please contact admin."
          : `Your agency account is ${user.status}. Please wait for admin approval.`;

      return res.status(403).json({
        success: false,
        message: msg,
        status: user.status,
        role: user.role,
      });
    }

    // Update last login for non-Agency roles before proceeding
    user.lastLogin = new Date();
    await user.save();

    // For non-Agency roles, return response directly
    res.status(200).json({
      success: true,
      token: generateToken(user._id),
      user: {
        id: user._id,
        name: user.name,
        email: user.email,
        role: user.role,
        status: user.status,
        priceOnCall: user.priceOnCall || false,
        showHideButton: user.showHideButton || false,
      },
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: error.message,
    });
  }
};

